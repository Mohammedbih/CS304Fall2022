package com.cs304.old.lab2dec;

/**
 * @author Saiko-Store
 */
public class Point {

  public double x;
  public double y;
  public float colorRed;
  public float colorBlue;
  public float colorGreen;

  public Point() {
  }

  public Point(double x, double y, float colorRed, float colorBlue, float colorGreen) {
    this.x = x;
    this.y = y;
    this.colorRed = colorRed;
    this.colorBlue = colorBlue;
    this.colorGreen = colorGreen;
  }
}
